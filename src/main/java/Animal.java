public class Animal{
    private String name;
    private String animal;
    private int id;
    
    public Animal(String name, String animal){
        this.name = name;
        this.animal = animal;
    }

    public String getName(){
        return name;
    }

    public String getAnimal(){
        return animal;
    }
    public int getId(){
        return id;
    }
}
